describe("is.fs.dir", function()
  local meta, is
  setup(function()
    meta = require "meta"
    is = require 'meta.is'
  end)
  it("meta", function()
    assert.truthy(is)
    assert.truthy(is.callable)
    assert.truthy(is.fs)
    assert.truthy(is.callable(is.fs.dir))
  end)
  describe("tests", function()
    it("cwd", function()
      assert.is_true(is.fs.dir(''))
      assert.is_true(is.fs.dir('.'))
    end)
    it("dir", function()
      assert.is_true(is.fs.dir('testdata'))
      assert.is_true(is.fs.dir('testdata/ok'))
      assert.is_true(is.fs.dir('/'))
      assert.is_true(is.fs.dir('/tmp'))
      assert.is_true(is.fs.dir('/var'))
      assert.is_true(is.fs.dir('testdata/loader/noinit'))
      assert.is_nil(is.fs.dir('testdata/loader/failed'))
    end)
    it("file", function() assert.is_nil(is.fs.dir('testdata/test')) end)
    it("file.empty", function() assert.is_nil(is.fs.dir('testdata/empty')) end)
    it("noneexistent", function() assert.is_nil(is.fs.dir('testdata/noneexistent')) end)
    it("dev_zero", function() assert.is_nil(is.fs.dir('/dev/zero')) end)
    it("dev_zero", function() assert.is_nil(is.fs.dir('/dev/null')) end)
  end)
end)