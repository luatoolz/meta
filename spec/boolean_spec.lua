describe("boolean", function()
  setup(function() require "meta" end)
  describe("toboolean", function()
    it("ok", function()
      assert.callable(toboolean)
      assert.equal(toboolean, require "meta.boolean")
    end)
    it("truthy", function()
      assert.is_true(toboolean(1))
      assert.is_true(toboolean(7))
      assert.is_true(toboolean(-1))
      assert.is_true(toboolean(-7))
      assert.is_true(toboolean('any'))
      assert.is_true(toboolean('true'))
      assert.is_true(toboolean('TRUE'))
      assert.is_true(toboolean(true))
      assert.is_true(toboolean({x=1}))
      assert.is_true(toboolean({{}}))
    end)
    it("falsy", function()
      assert.is_false(toboolean(0), '0')
      assert.is_false(toboolean(""), '""')
      assert.is_false(toboolean(' '))
      assert.is_false(toboolean('  '))
      assert.is_false(toboolean('	'))
      assert.is_false(toboolean('		'))
      assert.is_false(toboolean("	\r 	"))
      assert.is_false(toboolean(false), 'false')
      assert.is_false(toboolean(nil), 'nil')
      assert.is_false(toboolean(), 'nil')
      assert.is_false(toboolean({}), '{}')
      assert.is_false(toboolean("false"), '"false"')
      assert.is_false(toboolean("0"), '"0"')
      assert.is_false(toboolean("FALSE"), '"FALSE"')
    end)
  end)
end)