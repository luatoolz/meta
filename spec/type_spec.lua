describe("type", function()
  local meta, tt
--, mtype
  setup(function()
    meta = require "meta"
    tt = meta.type
--    mtype = require 'meta.module.type'
  end)
  it("loader", function()
    assert.is_table(meta)
    assert.is_table(meta.loader)
    assert.is_table(meta.module)
    assert.is_table(meta.is)
    assert.is_table(meta.mcache)
    assert.is_table(meta.seen)
    assert.is_table(meta.type)
    assert.callable(meta.call)

    assert.equal('loader', tt(meta))
    assert.equal('loader', tt(meta.loader))
    assert.equal('module', tt(meta.module))
    assert.equal('is', tt(meta.is))
    assert.equal('mcache', tt(meta.mcache))
    assert.equal('seen', tt(meta.seen))
    assert.equal('call', tt(meta.call))

--    assert.equal('type', mtype(meta.type))
    assert.equal('type', tt(meta.type))
  end)
  it("require .", function()
    assert.is_table(require('meta'))
    assert.is_table(require('meta.loader'))
    assert.is_table(require('meta.module'))
    assert.is_table(require('meta.is'))
    assert.is_table(require('meta.mcache'))
    assert.is_table(require('meta.seen'))
    assert.is_table(require('meta.type'))
    assert.callable(require('meta.call'))

    assert.equal('loader', tt(require('meta')))
    assert.equal('loader', tt(require('meta.loader')))
    assert.equal('module', tt(require('meta.module')))
    assert.equal('is', tt(require('meta.is')))
    assert.equal('mcache', tt(require('meta.mcache')))
    assert.equal('seen', tt(require('meta.seen')))
    assert.equal('call', tt(require('meta.call')))
    assert.equal('type', tt(require('meta.type')))
  end)
  it("require /", function()
    assert.is_table(require('meta'))
    assert.is_table(require('meta/loader'))
    assert.is_table(require('meta/module'))
    assert.is_table(require('meta/is'))
    assert.is_table(require('meta/mcache'))
    assert.is_table(require('meta/seen'))
    assert.is_table(require('meta/type'))
    assert.callable(require('meta/call'))

    assert.equal('loader', tt(require('meta')))
    assert.equal('loader', tt(require('meta/loader')))
    assert.equal('module', tt(require('meta/module')))
    assert.equal('is', tt(require('meta/is')))
    assert.equal('mcache', tt(require('meta/mcache')))
    assert.equal('seen', tt(require('meta/seen')))
    assert.equal('type', tt(require('meta/type')))
    assert.equal('call', tt(require('meta/call')))
  end)
  it("func addr", function()
    local a = string.lower
    local b = string.lower
    assert.equal(tostring(a), tostring(b))
  end)
end)